package org.openmrs.module.eptsreports.reporting.library.queries.data.quality;

public class Ec17Queries {

  /**
   * EC17 The date of drug pick up is before 1985
   *
   * @return
   */
  public static String getEc17CombinedQuery() {
    String query =
        " SELECT "
            + " pe.person_id As patient_id, "
            + " pid.identifier AS NID, "
            + " concat(ifnull(pn.given_name,''),' ',ifnull(pn.middle_name,''),' ',ifnull(pn.family_name,'')) AS Name, "
            + " DATE_FORMAT(pe.birthdate, '%d-%m-%Y') AS birthdate, "
            + " IF(pe.birthdate_estimated = 1, 'Sim','NÃ£o') AS Estimated_dob, "
            + " pe.gender AS Sex, DATE_FORMAT(pe.date_created, '%d-%m-%Y') AS First_entry_date, "
            + " DATE_FORMAT(pe.date_changed, '%d-%m-%Y') AS Last_updated, "
            + " DATE_FORMAT(programState.date_enrolled, '%d-%m-%Y') AS date_enrolled, "
            + " case "
            + " when programState.state = 9 then 'ABANDONO' "
            + " when programState.state = 6 then 'ACTIVO NO PROGRAMA' "
            + " when programState.state = 10 then 'OBITOU' "
            + " when programState.state = 8 then 'SUSPENDER TRATAMENTO' "
            + " when programState.state = 7 then 'TRANSFERIDO PARA' "
            + " when programState.state = 29 then 'TRANSFERIDO DE' "
            + " end AS state, "
            + " DATE_FORMAT(programState.start_date, '%d-%m-%Y') AS state_date, "
            + " DATE_FORMAT(filaANDrecepecao.encounter_datetime, '%d-%m-%Y') AS encounter_date, "
            + " filaANDrecepecao.FormType, "
            + " filaANDrecepecao.location_name AS location_name, "
            + "  IF(filaANDrecepecao.FormType = 'FILA',  DATE_FORMAT(filaANDrecepecao.encounter_datetime, '%d-%m-%Y'),'') AS fila_encounter_date, "
            + "  IF(filaANDrecepecao.FormType = 'RECEPCAO - LEVANTOU TARV',  DATE_FORMAT(filaANDrecepecao.encounter_datetime, '%d-%m-%Y'),'') AS recepcao_encounter_date, "
            + "  IF(filaANDrecepecao.FormType = 'FILA',  DATE_FORMAT(filaANDrecepecao.date_created, '%d-%m-%Y'),'') AS fila_date_created, "
            + "  IF(filaANDrecepecao.FormType = 'RECEPCAO - LEVANTOU TARV',  DATE_FORMAT(filaANDrecepecao.date_created, '%d-%m-%Y'),'') AS recepcao_date_created "
            + " FROM "
            + " person pe "
            + " INNER JOIN "
            + " (	select pn1.* "
            + " from person_name pn1 "
            + " inner join "
            + " ( "
            + " select person_id, min(person_name_id) id "
            + " from person_name "
            + " where voided = 0 "
            + " group by person_id "
            + " ) pn2 "
            + " where pn1.person_id = pn2.person_id and pn1.person_name_id = pn2.id "
            + " ) pn on pn.person_id = pe.person_id "
            + " INNER JOIN "
            + " ( select pid1.* "
            + " from patient_identifier pid1 "
            + " inner join "
            + " ( "
            + " select patient_id, min(patient_identifier_id) id "
            + " from patient_identifier "
            + " where voided = 0 "
            + " group by patient_id "
            + " ) pid2 "
            + " where pid1.patient_id = pid2.patient_id and pid1.patient_identifier_id = pid2.id "
            + " ) pid on pid.patient_id = pe.person_id "
            + " INNER JOIN "
            + " ( "
            + " Select p.patient_id, e.encounter_datetime, l.name  location_name, e.date_created, f.name AS FormType "
            + " from 	patient p "
            + " inner join encounter e on p.patient_id = e.patient_id "
            + " inner join location l on l.location_id = e.location_id "
            + " inner join form f on f.form_id = e.form_id and f.retired = 0 "
            + " where p.voided = 0 and e.voided = 0 and e.encounter_type = 18 and e.location_id IN (:location) "
            + " AND e.encounter_datetime < '1985-01-01 00:00:00' "
            + " UNION "
            + " SELECT p.patient_id, o.value_datetime encounter_datetime, l.name location_name, e.date_created, f.name AS FormType "
            + " FROM patient p "
            + " INNER JOIN encounter e on p.patient_id = e.patient_id "
            + " inner join form f on f.form_id = e.form_id and f.retired = 0 "
            + " inner join obs o on e.encounter_id = o.encounter_id "
            + " inner join location l on l.location_id = e.location_id "
            + " where p.voided = 0 and e.voided = 0 and o.voided = 0 and e.encounter_type = 52 "
            + " AND o.concept_id = 23866 and o.value_datetime is not null and e.location_id IN (:location) and o.value_datetime < '1985-01-01 00:00:00' "
            + " ) filaANDrecepecao on filaANDrecepecao.patient_id = pe.person_id "
            + "	LEFT JOIN ( "
            + " select max_estado.patient_id, max_estado.start_date, ps.state, pp.date_enrolled "
            + "	from( "
            + "			select pp.patient_id, max(max_estado.start_date) start_date,ps.patient_state_id,ps.state "
            + "			from( "
            + "				select pg.patient_id, ps.start_date,ps.voided,ps.state "
            + "				from patient p "
            + "						inner join patient_program pg on p.patient_id = pg.patient_id "
            + "				  	inner join patient_state ps on pg.patient_program_id = ps.patient_program_id "
            + "				where pg.voided=0  and p.voided=0 and  pg.program_id = 2 and ps.voided=0 "
            + "					and ps.start_date  <= :endDate  and pg.location_id=:location "
            + "                    order by pg.patient_id, ps.patient_state_id desc, ps.start_date desc "
            + "			)max_estado "
            + "				inner join patient_program pp on pp.patient_id = max_estado.patient_id "
            + "			 	inner join patient_state ps on ps.patient_program_id = pp.patient_program_id and ps.start_date = max_estado.start_date and max_estado.state=ps.state "
            + "	  		where pp.program_id = 2 and pp.voided = 0 and ps.voided = 0 and pp.location_id=:location "
            + "			group by pp.patient_id "
            + "	) max_estado "
            + "	inner join patient_state ps on ps.patient_state_id =max_estado.patient_state_id "
            + "	inner join patient_program pp on pp.patient_program_id = ps.patient_program_id "
            + "	) AS programState ON pe.person_id = programState.patient_id "
            + " where 	pe.voided = 0 ";
    return query;
  }
}
