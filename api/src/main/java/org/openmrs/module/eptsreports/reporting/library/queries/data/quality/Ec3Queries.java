package org.openmrs.module.eptsreports.reporting.library.queries.data.quality;

public class Ec3Queries {

  /**
   * Get the combine query for EC3 patient listing report
   *
   * @return String
   */
  public static String getEc3CombinedQuery() {
    String query =
        "SELECT pe.person_id As patient_id, "
            + " pid.identifier AS NID, "
            + " concat(ifnull(pn.given_name,''),' ',ifnull(pn.middle_name,''),' ',ifnull(pn.family_name,'')) AS Name, "
            + " DATE_FORMAT(pe.birthdate, '%d/%m/%Y') AS birthdate, "
            + " IF(pe.birthdate_estimated = 1, 'Sim','NÃ£o') AS estimated_dob, "
            + " pe.gender AS Sex, DATE_FORMAT(pe.date_created, '%d/%m/%Y') AS first_entry_date, "
            + " DATE_FORMAT(pe.date_changed, '%d/%m/%Y') AS last_updated, "
            + " DATE_FORMAT(pg.date_enrolled, '%d/%m/%Y') AS date_enrolled, "
            + " case "
            + " when ps.state = 9 then 'ABANDONO' "
            + " when ps.state = 6 then 'ACTIVO NO PROGRAMA' "
            + " when ps.state = 10 then 'OBITOU' "
            + " when ps.state = 8 then 'SUSPENDER TRATAMENTO' "
            + " when ps.state = 7 then 'TRANSFERIDO PARA' "
            + " when ps.state = 29 then 'TRANSFERIDO DE' "
            + " end AS state, "
            + " DATE_FORMAT(ps.start_date, '%d/%m/%Y') AS state_date, "
            + " DATE_FORMAT(pe.death_date,'%d/%m/%Y') As death_date_demographic, "
            + " DATE_FORMAT(deadP.deadPrograma,'%d/%m/%Y') As death_date_program, "
            + " DATE_FORMAT(deadP.deadFichaResumo,'%d/%m/%Y') As death_date_resumo, "
            + " DATE_FORMAT(deadP.deadFichaClinica,'%d/%m/%Y') As death_date_clinica, "
            + "  DATE_FORMAT(deadP.deadHomeVisitCard,'%d/%m/%Y') As death_date_home_vc, "
            + " IF(pe.death_date<fila.encounter_datetime OR deadP.deadPrograma<fila.encounter_datetime OR deadP.deadFichaResumo<fila.encounter_datetime OR deadP.deadFichaClinica<fila.encounter_datetime,DATE_FORMAT(fila.encounter_datetime, '%d/%m/%Y'),'') AS encounter_date_fila, "
            + " IF(pe.death_date<fila.date_created OR deadP.deadPrograma<fila.date_created OR deadP.deadFichaResumo<fila.date_created OR deadP.deadFichaClinica<fila.date_created OR deadP.deadHomeVisitCard<fila.date_created,DATE_FORMAT(fila.date_created, '%d/%m/%Y'),'') AS encounter_date_created_fila, "
            + " l.name AS location_name_fila "
            + " FROM "
            + " person pe "
            + " left join person peObito on pe.person_id = peObito.person_id and peObito.voided = 0 and peObito.death_date IS NOT NULL "
            + " left join "
            + " ( "
            + "		 select final.patient_id, "
            + "				MAX(CASE WHEN final.source = 'deadPrograma' then final.death_date END) as deadPrograma, "
            + "	            MAX(CASE WHEN final.source = 'deadFichaResumo' then final.death_date END) as deadFichaResumo, "
            + "	            MAX(CASE WHEN final.source = 'deadFichaClinica' then final.death_date END) as deadFichaClinica, "
            + "	            MAX(CASE WHEN final.source = 'deadHomeVisitCard' then final.death_date END) as deadHomeVisitCard from( "
            + " SELECT pg.patient_id AS patient_id, ps.start_date As death_date, 'deadPrograma' as source "
            + " FROM patient p "
            + " INNER JOIN patient_program pg ON p.patient_id = pg.patient_id "
            + " INNER JOIN patient_state ps ON pg.patient_program_id = ps.patient_program_id "
            + " WHERE p.voided = 0 AND pg.program_id = 2 "
            + " AND pg.voided = 0 "
            + " AND ps.voided = 0 "
            + " AND ps.state = 10 "
            + " AND pg.location_id =:location "
            + " AND ps.start_date IS NOT NULL AND ps.end_date IS NULL "
            + " AND ps.start_date <= :endDate "
            + " "
            + " union "
            + " "
            + "  SELECT p.patient_id AS patient_id, o.obs_datetime AS death_date, 'deadFichaResumo' as source "
            + " FROM patient p "
            + " INNER JOIN encounter e ON p.patient_id = e.patient_id "
            + " INNER JOIN obs o ON e.encounter_id = o.encounter_id "
            + " WHERE p.voided = 0 AND e.voided = 0 AND o.voided = 0 AND o.concept_id = 6272 "
            + " AND o.value_coded = 1366 "
            + " AND e.encounter_type = 53 "
            + " AND e.location_id =:location "
            + " AND o.obs_datetime <= :endDate "
            + " "
            + " union "
            + " "
            + "  SELECT p.patient_id AS patient_id, o.obs_datetime AS death_date, 'deadFichaClinica' as source "
            + " FROM patient p "
            + " INNER JOIN encounter e ON p.patient_id = e.patient_id "
            + " INNER JOIN obs o ON e.encounter_id = o.encounter_id "
            + " WHERE p.voided = 0 AND e.voided = 0 AND o.voided = 0 AND o.concept_id = 6273 "
            + " AND value_coded = 1366 "
            + " AND e.encounter_type = 6 "
            + " AND e.location_id =:location "
            + " AND o.obs_datetime <= :endDate "
            + " "
            + " union "
            + " "
            + "	select p.patient_id, "
            + "	max(e.encounter_datetime) death_date, 'deadHomeVisitCard' as source "
            + "from patient p "
            + "	inner join encounter e on p.patient_id = e.patient_id "
            + "	inner join obs o on o.encounter_id = e.encounter_id "
            + "where e.voided = 0 and p.voided = 0 and e.encounter_datetime <=:endDate and e.encounter_type = 21 "
            + "	and o.voided = 0 and o.concept_id in (2031,23944,23945) and o.value_coded = 1366 and e.location_id=:location "
            + "	group by p.patient_id "
            + "	)final group by final.patient_id "
            + " ) deadP on pe.person_id = deadP.patient_id "
            + " left join "
            + " ( select pn1.* "
            + " from person_name pn1 "
            + " inner join "
            + " ( "
            + " select person_id, min(person_name_id) id "
            + " from person_name "
            + " where voided = 0 "
            + " group by person_id "
            + " ) pn2 "
            + " where pn1.person_id = pn2.person_id and pn1.person_name_id = pn2.id "
            + " ) pn on pn.person_id = pe.person_id "
            + " left join "
            + " ( select pid1.* "
            + " from patient_identifier pid1 "
            + " inner join "
            + " ( "
            + " select patient_id, min(patient_identifier_id) id "
            + " from patient_identifier "
            + " where voided = 0 "
            + " group by patient_id "
            + " ) pid2 "
            + " where pid1.patient_id = pid2.patient_id and pid1.patient_identifier_id = pid2.id "
            + " ) pid on pid.patient_id = pe.person_id "
            + " left join location l on l.location_id = pid.location_id "
            + " left join "
            + " ( "
            + " Select p.patient_id, e.encounter_datetime, l.name  location_name, e.date_created "
            + " from patient p "
            + " inner join encounter e on p.patient_id = e.patient_id "
            + " inner join location l on l.location_id = e.location_id "
            + " where 	p.voided = 0 and e.voided = 0 and e.encounter_type = 18 "
            + " and e.location_id =:location "
            + " AND e.encounter_datetime between :startDate AND :endDate "
            + " ) fila 	on fila.patient_id = pe.person_id "
            + " left join  patient_program pg ON pe.person_id = pg.patient_id and pg.program_id = 2 "
            + " and pg.location_id =:location "
            + "	LEFT JOIN ( "
            + " select max_estado.patient_id, max_estado.start_date, ps.state "
            + "	from( "
            + "			select pp.patient_id, max(max_estado.start_date) start_date,ps.patient_state_id,ps.state "
            + "			from( "
            + "				select pg.patient_id, ps.start_date,ps.voided,ps.state "
            + "				from patient p "
            + "						inner join patient_program pg on p.patient_id = pg.patient_id "
            + "				  	inner join patient_state ps on pg.patient_program_id = ps.patient_program_id "
            + "				where pg.voided=0  and p.voided=0 and  pg.program_id = 2 and ps.voided=0 "
            + "					and ps.start_date  <= :endDate  and pg.location_id=:location "
            + "                    order by pg.patient_id, ps.patient_state_id desc, ps.start_date desc "
            + "			)max_estado "
            + "				inner join patient_program pp on pp.patient_id = max_estado.patient_id "
            + "			 	inner join patient_state ps on ps.patient_program_id = pp.patient_program_id and ps.start_date = max_estado.start_date and max_estado.state=ps.state "
            + "	  		where pp.program_id = 2 and pp.voided = 0 and ps.voided = 0 and pp.location_id=:location "
            + "			group by pp.patient_id "
            + "	) max_estado "
            + "	inner join patient_state ps on ps.patient_state_id =max_estado.patient_state_id "
            + "	inner join patient_program pp on pp.patient_program_id = ps.patient_program_id "
            + "	) AS ps ON pe.person_id = ps.patient_id "
            + " where pe.voided = 0 and "
            + " ( "
            + " peObito.person_id is not null or "
            + " deadP.deadPrograma is not null or "
            + " deadP.deadFichaResumo is not null or "
            + " deadP.deadFichaClinica is not null or "
            + " deadP.deadHomeVisitCard is not null "
            + " ) and "
            + " ( "
            + " ( "
            + " fila.encounter_datetime>peObito.death_date or "
            + " fila.encounter_datetime>deadP.deadPrograma or "
            + " fila.encounter_datetime>deadP.deadFichaResumo or "
            + " fila.encounter_datetime>deadP.deadFichaClinica "
            + " ) "
            + " ) "
            + " GROUP BY pe.person_id ";
    return query;
  }
}
